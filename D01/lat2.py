# Program Penjumlahan Dua Bilangan 
# Inputan dari user
bilangan1 = input('Masukkan bilangan pertama: ') 
bilangan2 = input('Masukkan bilangan kedua: ') 
# Menjumlahkan bilangan 
jumlah = int(bilangan1) + int(bilangan2) 
# Menampilkan jumlah 
print('Jumlah {0} + {1} adalah {2}'.format(bilangan1, bilangan2, jumlah))
